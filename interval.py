    A4 = 6  # Augmented Fourth (or Diminished Fifth)
    P5 = 7  # Perfect Fifth
    m6 = 8  # Minor Sixth
    M6 = 9  # Major Sixth
    m7 = 10  # Minor Seventh
    M7 = 11  # Major Seventh
    P8 = 12  # Perfect Octave
